#include <string.h>
#include <wx/strconv.h>
#include "output-stream.h"

namespace Wz {
	unsigned int
	OutputStream::write(wxString &data) {
		const char *str = wxConvUTF8.cWX2MB(data.c_str());
		return write(str, strlen(str));
	}
}
